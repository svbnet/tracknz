package co.svbnet.tracknz;

/**
 * Contains preference key names
 */
public class PreferenceKeys {
    /**
     * If notifications are enabled. Stored as boolean.
     */
    public static final String NOTIFICATIONS_ENABLED = "notifications_enabled";
    /**
     * Whether to vibrate on notifications. Stored as boolean.
     */
    public static final String NOTIFICATIONS_VIBRATE = "notifications_vibrate";
    /**
     * Whether notifications make a sound. Stored as boolean.
     */
    public static final String NOTIFICATIONS_SOUND = "notifications_sound";
    /**
     * Whether notifications blink the LED light. Stored as boolean.
     */
    public static final String NOTIFICATIONS_LIGHT = "notifications_light";
}
